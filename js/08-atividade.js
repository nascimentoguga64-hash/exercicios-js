let dia = Number(prompt("Número de 1 a 7"))

switch (dia) {
case 1: console.log("Domingo")
alert(`Domingo!`)
break

case 2: console.log("Segunda")
alert(`Segunda!`)
break

case 3: console.log("Terça")
alert(`Terça!`)
break

case 4: console.log("Quarta!")
alert(`Quarta!`)
break

case 5: console.log("Quinte")
alert(`Quinta!`)
break

case 6: console.log("Sexta")
alert(`Sexta`)
break

case 7: console.log("Sábado")
alert(`Sábado`)
break

default: console.log("Inválido")
break
}